package datatypeprogrammes;

public class concatenation {




    public static void  main(String[] args)
    {
        System.out.println(40+20);//60
        System.out.println(10.25+30);//40.25
        System.out.println(15.35+25.35);//40.75
        System.out.println(25+"java");//25java
        System.out.println(5+10+"sql");//15sql
        System.out.println("JSE"+12+25+36);//JSE73
        System.out.println("JSE"+(12+25+36));//JSE73
        System.out.println('J'+'A');//JA
        System.out.println('A'+20);//A20
        System.out.println('C'+"JAVA");//CJAVA
        System.out.println(25+"true");//25true
        //System.out.println(25+true);//error
        System.out.println("25"+"10");//2510




    }


}
